    <h2>What is the main concept of data processing?</h2>
    <p>
        Data processing occurs when data is collected and translated into usable information. Usually performed by a data scientist or team of data scientists, it is important for data processing to be done correctly as not to negatively affect the end product, or data output.<br><br>
    </p>
    <h2>What are the 3 types of processing data?</h2>
    <p>
        There are three main data processing methods - manual, mechanical and electronic.<br><br>
    </p>
