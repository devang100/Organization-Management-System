    <h2>Computer Graphic Design Course Skills-Overview</h2>
    <p>
        Courses in computer graphic design, given through programs found in community colleges, colleges and universities, provide students with the skills needed to perform tasks such as website design, photo editing and other graphic arts functions. Students learn how to maintain websites, edit and process photos and images, craft multi-dimensional images, use popular software, develop designs and draw. They also learn about electronic production, animation, color theory, video, file management, layout and programming.<br><br>
    </p>
    <h2>Common Computer Graphic Design Courses</h2>
    <p>
        Although specific course names may vary, curriculum is often similar across institutions. Naturally, units at the graduate level may be more advanced. Typical central classes cover a wide range of related topics, including:<br><br>
        <b>Web Publishing: </b>Students develop their skills in website design, implementation and maintenance as well as honing their skills in using HTML and website publishing programs, such as DreamWeaver.<br><br>
        <b>Computer Graphic Design: </b>This course improves students' skills using photo editing and paint programs as well as their basic computer and technical skills.<br><br>
        <b>2D and 3D Design: </b>Students develop their skills in designing and constructing 2D and 3D forms.<br><br>
        <b>Digital Photography: </b>This course involves photographic processing skills along with image manipulation and creation skills.<br><br>
        <b>Digital Editing: </b>Students acquire skills in editing and manipulating animated images.<br><br>
        <b>Desktop Publishing Fundamentals: </b>Skills developed in this course include design techniques, electronic production, image and file management, printing and page layout. Students acquire skills in using programs such as PageMaker and QuarkXPress.<br><br>
    </p>
